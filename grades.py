# برنامج تقدير درجة الطالب
# يتيح للمستخدم إدخال درجة الطالب ويعطيه التقدير المناسب
# يستمر البرنامج حتى يكتب المستخدم "خروج"

def get_grade_estimation(score):
    """
    تحويل الدرجة المئوية إلى تقدير نصي
    
    المعاملات:
    score (float): درجة الطالب بين 0 و 100
    
    المخرجات:
    str: التقدير المناسب (ضعيف، جيد، جيد جداً، ممتاز)
    """
    if score >= 85:
        return "ممتاز"
    elif score >= 75:
        return "جيد جداً"
    elif score >= 65:
        return "جيد"
    else:
        return "ضعيف"

def main():
    """
    الوظيفة الرئيسية للبرنامج
    تتعامل مع إدخال المستخدم وتستمر حتى يطلب الخروج
    """
    print("=" * 50)
    print("مرحباً بك في برنامج تقدير درجات الطلاب")
    print("=" * 50)
    print("أدخل درجة الطالب (من 0 إلى 100)")
    print("لإنهاء البرنامج، اكتب 'خروج'")
    print("-" * 50)
    
    while True:
        # طلب إدخال من المستخدم
        user_input = input("\nأدخل الدرجة أو 'خروج': ").strip()
        
        # شرط الخروج من البرنامج
        if user_input == "خروج":
            print("\nشكراً لاستخدام البرنامج. إلى اللقاء!")
            break
        
        # محاولة تحويل الإدخال إلى رقم
        try:
            score = float(user_input)
        except ValueError:
            print("❌ خطأ: الرجاء إدخال رقم صحيح أو كلمة 'خروج'")
            continue  # العودة لبداية الحلقة
        
        # التحقق من صحة نطاق الدرجة
        if score < 0 or score > 100:
            print("❌ خطأ: الدرجة يجب أن تكون بين 0 و 100 فقط")
            continue  # العودة لبداية الحلقة
        
        # حساب التقدير وعرضه
        estimation = get_grade_estimation(score)
        print(f"✅ الدرجة {score} تقديرها: {estimation}")

# تشغيل البرنامج
if __name__ == "__main__":
    main()
